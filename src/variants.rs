/// Returns the static variant→canonical character map.
///
/// Each entry maps a non-standard / old-form / variant CJK character to its
/// canonical (simplified / new-form) counterpart used as the normalization target.
///
/// Coverage:
/// - JIS X 0213 旧字体 → 新字体 (primary target)
/// - IVD representative base characters
/// - Common Traditional Chinese → Japanese new-form equivalents
/// - Frequently seen name/place kanji variants
pub fn variant_map() -> &'static HashMap<char, char> {
    static MAP: OnceLock<HashMap<char, char>> = OnceLock::new();
    MAP.get_or_init(build_map)
}

